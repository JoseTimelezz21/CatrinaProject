    private void cbxMonthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxMonthActionPerformed
/**
 * Creates an instance of the Operations class.
 * Set up a specific currency format for Mexico (es_MX) using the NumberFormat.
 * Gets the index for the selected month in a chart component (cbxMonth).
 * Updates a graphic interface label "lblInitialBalance" with the calculated initial balance for the selected month.
 * Calls a "sortList" method of the "Operations" class instance to get and display filtered and sorted data in a table (tblRegisters).
 * Updates graphical interface labels with information about withdrawals, deposits, ending balance, and ending balance for the period.
 * Handles any exception that may occur during this process and displays an error message in the console in case any exception occurs.
 */
        try{
        Operations operations = new Operations();
        Locale local = new Locale("es","MX");
        NumberFormat currencyFormat = NumberFormat.getCurrencyInstance(local);
        int month = cbxMonth.getSelectedIndex();
                
        lblInitialBalance.setText("INITIAL BALANCE: "+currencyFormat.format(operations.calculateInitialBalance(month, accountStatement)));                
       operations.showFilteredData(operations.sortList(month, accountStatement), currencyFormat, tblRegisters, operations.calculateInitialBalance(month, accountStatement));
                             
        lblWithDrawals.setText("WITHDRAWALS: "+ currencyFormat.format(operations.sumWithDrawals(operations.sortList(month, accountStatement))));
        lblDeposits.setText("DEPOSITS: " + currencyFormat.format(operations.sumWithDrawals(operations.sortList(month, accountStatement))));
        lblFinalBalance.setText("FINAL BALANCE: " + operations.calculateFinalBalance(tblRegisters));
        lblFinalPeriod.setText("FINAL BALANCE OF THE PERIOD: " + operations.calculateFinalBalance(tblRegisters));
        }catch(Exception ex){
            System.err.print("An error occurred: " + ex.getMessage());
        }            
    }//GEN-LAST:event_cbxMonthActionPerformed

